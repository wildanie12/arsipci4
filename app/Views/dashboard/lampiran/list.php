<script>
    $(document).ready(function () {

        let dataParams = {
            limit: 50,
            berdasarkan: 'file',
            sifat_penempatan: 'eksternal',
            view_content: 'dashboard/lampiran/ajax/table',
            view_pagination: 'dashboard/lampiran/ajax/pagination'
        }
        function refreshData(url) {
            url = 0 || url
            if (!url) url = "<?= site_url('resource/lampiran') ?>"
            $(".progress-data").css('visibility', 'visible');
            $.ajax({
                type: "GET",
                url: url,
                data: dataParams,
                dataType: "json",
            })
            .done(function (response) {
                $(".progress-data").css('visibility', 'hidden');
                $(".load-data").html(response.content)
                $(".load-pagination").html(response.pagination)
                onLoadedData()
            })
        }
        function onLoadedData() {
            $(".btn-pagination").unbind('click').click(function (e) {
                e.preventDefault()
                link = $(this).attr('href')
                refreshData(link)
            })
            $(".btn-delete").unbind('click').click(function (e) {
                e.preventDefault()
                if (confirm('Anda yakin?')) {
                    id = $(this).data('id')
                    $.ajax({
                        type: "POST",
                        url: "<?= site_url('resource/lampiran/') ?>" + id,
                        data: {
                            _method: 'DELETE',
                            ['<?= csrf_token() ?>']: '<?= csrf_hash() ?>'
                        },
                        dataType: "json",
                    })
                    .done(function (response) {
                        refreshData()
                    })
                }
            })
        }
        refreshData()

        let modalNewLampiran = $("#modal-new-lampiran")
        $(".btn-new-lampiran").click(function (e) {
            e.preventDefault()
            modalNewLampiran.modal('show')
        })

        modalNewLampiran.on('shown.bs.modal', function() {
            modalNewLampiran.find('form').unbind('submit').submit(function (e) {
                e.preventDefault()
                modalNewLampiran.find('.loading').show()
                modalNewLampiran.find(".error-message").hide()
                formData = new FormData(this)
                $.ajax({
                    type: "POST",
                    url: "<?= site_url('resource/lampiran') ?>",
                    data: formData,
                    dataType: "json",
                    contentType: false,
                    processData: false,
                    cache: false
                })
                .done(function(response) {
                    if (response.status == 'error') {
                        Object.entries(response.errors).forEach((error) => {
                            const [field, message] = error
                            modalNewLampiran.find('.error-' + field).html(message).show()
                        })
                    } else {
                        modalNewLampiran.find('form')[0].reset()
                        modalNewLampiran.modal('hide')
                        modalNewLampiran.find(".error-message").hide()
                        modalNewLampiran.find(".preview-image").hide()
                        refreshData()
                    }
                })
                .always(function() {
                    modalNewLampiran.find('.loading').hide()
                })
            })
        })

        $(".filter-pencarian").on('input', function (e) {
            value = $(this).val()
            if (value != '' ) {
                dataParams.pencarian = value
            } else {
                delete dataParams.pencarian
            }
            refreshData()
        })
        $(".filter-limit").change(function (e) {
            value = $(this).val()
            if (value != '' ) {
                dataParams.limit = value
            } else {
                delete dataParams.limit
            }
            refreshData()
        })
        $(".filter-berdasarkan").change(function (e) {
            value = $(this).val()
            if (value != '' ) {
                dataParams.berdasarkan = value
            } else {
                delete dataParams.berdasarkan
            }
            refreshData()
        }) 
        $(".filter-kompetensi_teknis").change(function (e) {
            value = $(this).val()
            if (value != '') {
                dataParams.kompetensi_teknis = value
            } else {
                delete dataParams.kompetensi_teknis
            }
            refreshData()
        })
        $(".filter-tanggal_sertifikat").change(function (e) {
            value = $(this).val()
            if (value != '') {
                dataParams.tanggal_sertifikat = value
            } else {
                delete dataParams.tanggal_sertifikat
            }
            refreshData()
        })
        $(".filter-status_sertifikat").change(function (e) {
            value = $(this).val()
            if (value != '') {
                dataParams.status_sertifikat = value
            } else {
                delete dataParams.status_sertifikat
            }
            refreshData()
        })

        $(".btn-sort").click(function (e) {
            e.preventDefault()
            field = $(this).attr('data-field')
            sort = $(this).attr('data-sort')
            $(".btn-sort").removeClass('text-danger')
            $(".btn-sort").addClass('text-warning')
            $(this).addClass('text-danger')

            if (sort == 'asc') {
                sort = 'desc'
                $(this).attr('data-sort', 'desc')
                $(this).find('i').removeClass('fa-sort-amount-down-alt')
                $(this).find('i').addClass('fa-sort-amount-up-alt')
            } else {
                sort = 'asc'
                $(this).attr('data-sort', 'asc')
                $(this).find('i').removeClass('fa-sort-amount-up-alt')
                $(this).find('i').addClass('fa-sort-amount-down-alt')
            }

            dataParams.sort = sort
            dataParams.sortBy = field
            refreshData()
        })
    })
</script>

<?= $this->endSection(); ?>
